	/**
	 * EvaluateHand is a static method that will score a given Hand of cards
	 * 
	 * @param h
	 * @return
	 * @throws HandException
	 */
	public static Hand EvaluateHand(Hand h) throws HandException { // method for
																	// evaluating
																	// the hand

		Collections.sort(h.getCardsInHand()); // Sorts the hand of cards,
												// highest being FirstCard,
												// lowest being FifthCard

		// Collections.sort(h.getCardsInHand(), Card.CardRank);

		if (h.getCardsInHand().size() != 5) {
			throw new HandException(h);
		}

		HandScore hs = new HandScore();
		try {
			Class<?> c = Class.forName("pokerBase.Hand");

			for (eHandStrength hstr : eHandStrength.values()) {
				Class[] cArg = new Class[2];
				cArg[0] = pokerBase.Hand.class;
				cArg[1] = pokerBase.HandScore.class;

				Method meth = c.getMethod(hstr.getEvalMethod(), cArg);
				Object o = meth.invoke(null, new Object[] { h, hs });

				// If o = true, that means the hand evaluated- skip the rest of
				// the evaluations
				if ((Boolean) o) {
					break;
				}
			}

			h.bScored = true;
			h.HandScore = hs;

		} catch (ClassNotFoundException x) {
			x.printStackTrace();
		} catch (IllegalAccessException x) {
			x.printStackTrace();
		} catch (NoSuchMethodException e) {
			System.out.println(e.getMessage());
			e.printStackTrace();
		} catch (SecurityException e) {
			e.printStackTrace();
		} catch (IllegalArgumentException e) {
			e.printStackTrace();
		} catch (InvocationTargetException e) {
			e.printStackTrace();
		}
		return h;
	}

	private static boolean boolAce(ArrayList<Card> Cards) { // To test for if
															// there is an ACE
															// present in the
															// deck

		int count = 0;
		boolean bAce = false;
		for (eRank rank : eRank.values()) {
			for (Card c : Cards) {
				if (c.geteRank() == eRank.ACE) {
					count++;
				}
			}
			if (count >= 1)
				bAce = true;
		}
		return bAce;
	}

	private static boolean isFlush(ArrayList<Card> Cards) { // A helper function
															// made to test if a
															// hand is Flush, 5
															// similar suits

		int fCount = 0;
		boolean bFlushHand = false;
		for (eSuit Suit : eSuit.values()) {
			fCount = 0;
			for (Card c : Cards) {
				if (c.geteSuit() == Suit) {
					fCount++;
				}
			}
			if (fCount == 5)
				bFlushHand = true;
		}
		return bFlushHand;
	}

